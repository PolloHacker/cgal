/** \brief Runs mean-curvature-flow skeleton extraction on a closed triangle mesh.
 *
 * The input mesh must be a closed surface mesh.
 *
 * The function does not modify the input mesh, but extracts the skeleton into the output parameter.
 */
bool skeletonize(Triangle_mesh &mesh, Skeleton &skeleton)
{
  log_stage("3. Mean curvature flow skeletonization");

  // The mean curvature flow skeletonization algorithm a method that works
  // by simulating cloth on the surface of the mesh, which gradually contracts it while preserving its topology.
  CGAL::extract_mean_curvature_flow_skeleton(mesh, skeleton);

  std::cout << "Skeleton vertices: " << boost::num_vertices(skeleton) << "\n";
  std::cout << "Skeleton edges: " << boost::num_edges(skeleton) << "\n";

  return boost::num_vertices(skeleton) > 0 && boost::num_edges(skeleton) > 0;
}

/** \brief Writes maximal skeleton polylines as one polyline per row. */
bool write_skeleton_polylines(const fs::path &out_path, const Skeleton &skeleton)
{
  std::ofstream out(out_path.string());
  if (!out)
  {
    std::cerr << "Error: cannot open " << out_path.string() << " for writing.\n";
    return false;
  }

  Display_polylines display(skeleton, out);
  CGAL::split_graph_into_polylines(skeleton, display);
  return true;
}

/** \brief Writes the skeleton as a plain edge list (segment endpoints).
 *
 * The plain edge list is good for applications that only need the skeleton connectivity and geometry,
 * without the maximal polyline structure.
 */
bool write_skeleton_edges(const fs::path &out_path, const Skeleton &skeleton)
{
  std::ofstream out(out_path.string());
  if (!out)
  {
    std::cerr << "Error: cannot open " << out_path.string() << " for writing.\n";
    return false;
  }

  out << "# x1 y1 z1 x2 y2 z2\n";
  for (const Skeleton_edge edge : CGAL::make_range(edges(skeleton)))
  {
    const Skeleton_vertex source_v = source(edge, skeleton);
    const Skeleton_vertex target_v = target(edge, skeleton);
    out << skeleton[source_v].point << " " << skeleton[target_v].point << "\n";
  }
  return true;
}

/** \brief Writes vertex-to-skeleton correspondence as line segments. */
bool write_correspondence(const fs::path &out_path,
                          const Skeleton &skeleton,
                          const Triangle_mesh &mesh)
{
  std::ofstream out(out_path.string());
  if (!out)
  {
    std::cerr << "Error: cannot open " << out_path.string() << " for writing.\n";
    return false;
  }

  // The skeletonization algorithm computes a set of vertices on the input mesh that correspond to each skeleton vertex.
  // This function writes those correspondences as line segments from the skeleton vertex to each corresponding mesh.
  for (const Skeleton_vertex v : CGAL::make_range(vertices(skeleton)))
  {
    for (const mesh_vertex_descriptor vd : skeleton[v].vertices)
    {
      out << "2 " << skeleton[v].point << " " << get(CGAL::vertex_point, mesh, vd) << "\n";
    }
  }
  return true;
}

/** \brief Writes all skeleton artifacts to disk. */
bool write_skeleton_outputs(const Output_paths &paths,
                            const Skeleton &skeleton,
                            const Triangle_mesh &mesh)
{
  log_stage("3.1 Export skeleton artifacts");

  if (!write_skeleton_polylines(paths.skeleton_polylines, skeleton))
  {
    return false;
  }

  if (!write_skeleton_edges(paths.skeleton_edges, skeleton))
  {
    return false;
  }

  if (!write_correspondence(paths.correspondence, skeleton, mesh))
  {
    return false;
  }

  std::cout << "Saved: " << paths.skeleton_polylines.string() << "\n";
  std::cout << "Saved: " << paths.skeleton_edges.string() << "\n";
  std::cout << "Saved: " << paths.correspondence.string() << "\n";
  return true;
}

/** \brief point-cloud to skeleton pipeline. */
int main(int argc, char *argv[])
{
#ifndef CGAL_EIGEN3_ENABLED
  std::cerr << "Error: CGAL_EIGEN3_ENABLED is not defined. "
            << "Install Eigen and configure CGAL with Eigen support.\n";
  return EXIT_FAILURE;
#endif

  Pipeline_options options;
  if (!parse_args(argc, argv, options))
  {
    return EXIT_FAILURE;
  }

  if (!prepare_output_dir(options.output_dir))
  {
    return EXIT_FAILURE;
  }

  const Output_paths output_paths = make_output_paths(options);

  std::vector<Pwn> points;
  if (!load_oriented_points(options, points))
  {
    return EXIT_FAILURE;
  }

  if (!write_point_stage_visualization(
          output_paths.raw_points,
          points,
          "1.1 Visualize raw input point cloud"))
  {
    return EXIT_FAILURE;
  }

  double average_spacing = 0.0;
  if (!preprocess_points(points, options, average_spacing))
  {
    return EXIT_FAILURE;
  }

  if (!write_point_stage_visualization(
          output_paths.preprocessed_points,
          points,
          "1.3 Visualize preprocessed point cloud"))
  {
    return EXIT_FAILURE;
  }

  Triangle_mesh mesh;
  log_stage("2. Poisson reconstruction");
  if (!mesh_reconstruction::reconstruct_mesh_poisson(points, average_spacing, mesh))
  {
    return EXIT_FAILURE;
  }

  if (!write_mesh_stage_visualization(
          output_paths.raw_reconstructed_mesh,
          mesh,
          "2.1 Visualize raw Poisson reconstruction"))
  {
    return EXIT_FAILURE;
  }

  log_stage("2.2 Mesh normalization and validation");
  if (!mesh_reconstruction::normalize_mesh_for_skeletonization(mesh, options.keep_largest_component))
  {
    return EXIT_FAILURE;
  }

  if (!write_mesh_stage_visualization(
          output_paths.reconstructed_mesh,
          mesh,
          "2.3 Export normalized reconstructed mesh"))
  {
    return EXIT_FAILURE;
  }

  Skeleton skeleton;
  if (!skeletonize(mesh, skeleton))
  {
    std::cerr << "Error: skeleton extraction produced an empty skeleton.\n";
    return EXIT_FAILURE;
  }

  if (!write_skeleton_outputs(output_paths, skeleton, mesh))
  {
    return EXIT_FAILURE;
  }

  std::cout << "\nPipeline completed successfully.\n";
  return EXIT_SUCCESS;
}
